namespace StillGoodToGo.Enum
{
    /// <summary>
    /// Defines the roles available in the system.
    /// </summary>
    public enum Role
    {
        /// <summary>
        /// Represents an administrator with full access to the system.
        /// </summary>
        Admin,

        /// <summary>
        /// Represents a client who can browse and interact with establishments.
        /// </summary>
        Client,

        /// <summary>
        /// Represents an establishment owner or manager.
        /// </summary>
        Establishment
    }
}